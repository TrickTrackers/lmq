            function createStickyTimeline(sectionId, leftColumnClass) {

                const section = document.getElementById(sectionId);

                const leftColumn = section.querySelector(`.${leftColumnClass}`);



                const timeline = gsap.timeline({

                    scrollTrigger: {

                        trigger: section,

                        start: "top top",

                        end: "bottom top",

                        pin: leftColumn,

                        scrub: true

                    }

                });



                return timeline;

            }



